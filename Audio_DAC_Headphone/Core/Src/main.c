void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM2)
	{
		//mySignal = sinf(i_t * 2 * PI * sample_dt);
		mySignal = 5 * sinf(i_t * 2 * 3 * sample_dt);
		noise = 5 * sinf(i_t * 2 * 50 * 3 * sample_dt);
		ref_noise = 5 * sinf(i_t * 2 * 50 * 3 * sample_dt + 3/20);
		noise_mySignal =  mySignal + noise;

	    a = noise_mySignal - (ref_noise * adap_idx_0 + delayed_idx_1 *
	                      adap_idx_1);
	    result_mySignal = a;
	    a *= 0.01;
	    adap_idx_0 += a * ref_noise;
	    adap_idx_1 += a * delayed_idx_1;
	    delayed_idx_1 = ref_noise;

		//convert from float to decimal
	    if(btn_pused == 1)
	    {
	    	dac_op_val = (noise_mySignal + 1) * 127;
	    }
	    else
	    {
	    	dac_op_val = (result_mySignal + 1) * 127;
	    }

		//op the sample to STM DAC
		HAL_DAC_SetValue(&hdac, DAC_CHANNEL_1, DAC_ALIGN_8B_R, dac_op_val);

		i_t++;
		if(i_t >= sample_N) i_t = 0;
	}
}
